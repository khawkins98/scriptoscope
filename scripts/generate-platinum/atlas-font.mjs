export const FONT = { w: 5, h: 7, gap: 1 };

/**
 * Draw a string into an RGBA image at (x,y) in color `c`. Unknown chars (incl.
 * the em dash) render as a space. `set` is the caller's pixel setter.
 */
export function drawText(set, x, y, text, c) {
  let cx = x;
  for (const ch of text.toUpperCase()) {
    const g = G[ch] ?? G[' '];
    for (let ry = 0; ry < FONT.h; ry++) {
      const row = g[ry];
      for (let rx = 0; rx < FONT.w; rx++) if (row[rx] === '#') set(cx + rx, y + ry, c);
    }
    cx += FONT.w + FONT.gap;
  }
  return cx; // advance
}
